#include <iostream>
using namespace std;

int main()
{
    //常用转义字符

    //换行符 \n
    cout << "hello word\n";

    //反斜杠 \\

    cout << "\\" << endl;

    //水平制表符 \t
    cout << "aaaa\thello world" << endl;
    cout << "aa\thello world" << endl;
    cout << "aaaaaa\thello world" << endl;

    return 0;
}